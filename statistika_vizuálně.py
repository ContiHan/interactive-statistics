def plot_q_statistic(n_common_correct, n_only_a_wrong, n_only_b_wrong, n_both_wrong):
    """Vizualizace Q-statistiky pro dva klasifikátory."""
    # Compute Q coefficient (Q = (ad - bc) / (ad + bc))
    a = n_common_correct
    b = n_only_b_wrong
    c = n_only_a_wrong
    d = n_both_wrong

    numerator = a * d - b * c
    denominator = a * d + b * c
    q_val = numerator / denominator if denominator != 0 else 0

    # Build 2x2 confusion matrix for heatmap
    data = [[a, b], [c, d]]

    plt.figure(figsize=(8, 6))
    sns.heatmap(data, annot=True, fmt='d', cmap='RdYlGn_r',
                xticklabels=['B Správně', 'B Chyba'],
                yticklabels=['A Správně', 'A Chyba'])
    plt.title(f'Matice chyb modelů A a B\nQ-statistika = {q_val:.3f}')
    plt.show()


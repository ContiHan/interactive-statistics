def plot_pdf_cdf_link(dist_type, mu, sigma, lam, x_val):
    """Vykreslení hustoty (PDF) a distribuční funkce (CDF) normálního nebo exponenciálního rozdělení vedle sebe."""
    if dist_type == 'Normální':
        dist = stats.norm(loc=mu, scale=sigma)
        x = np.linspace(mu - 4*sigma, mu + 4*sigma, 500)
        start_x = mu - 4*sigma
    else:
        dist = stats.expon(scale=1/lam)
        x = np.linspace(0, 10/lam, 500)
        start_x = 0
    pdf = dist.pdf(x)
    cdf = dist.cdf(x)
    current_cdf = dist.cdf(x_val)
    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(15, 5))
    ax1.plot(x, pdf, lw=2, color='royalblue', label='Hustota (PDF)')
    x_fill = np.linspace(start_x, x_val, 100)
    ax1.fill_between(x_fill, dist.pdf(x_fill), alpha=0.3, color='royalblue')
    ax1.axvline(x_val, color='crimson', linestyle='--')
    ax1.set_title(f'Hustota pravděpodobnosti (Plocha = {current_cdf:.3f})')
    ax1.set_xlabel('x')
    ax1.legend()
    ax2.plot(x, cdf, lw=2, color='seagreen', label='Distribuční f. (CDF)')
    ax2.axvline(x_val, color='crimson', linestyle='--')
    ax2.axhline(current_cdf, color='crimson', alpha=0.3, linestyle=':')
    ax2.scatter([x_val], [current_cdf], color='crimson')
    ax2.set_title(f'Distribuční funkce (F(x) = {current_cdf:.3f})')
    ax2.set_xlabel('x')
    ax2.set_ylabel('F(x)')
    ax2.legend()
    plt.tight_layout()
    plt.show()

def plot_hypothesis_errors(mu_alt, alpha, sigma):
    """Vizualizace chyby I. a II. druhu."""
    mu0 = 0
    critical_val = stats.norm.ppf(1 - alpha, mu0, sigma)
    x = np.linspace(-4, mu_alt + 4, 1000)
    y0 = stats.norm.pdf(x, mu0, sigma)
    y1 = stats.norm.pdf(x, mu_alt, sigma)
    beta = stats.norm.cdf(critical_val, mu_alt, sigma)
    mask_alpha = x >= critical_val
    mask_beta = x <= critical_val
    plt.figure(figsize=(12, 6))
    plt.plot(x, y0, label='H0 (Nulová)', color='royalblue', lw=2)
    plt.plot(x, y1, label='H1 (Alternativní)', color='crimson', lw=2)
    plt.fill_between(x[mask_alpha], y0[mask_alpha], color='royalblue', alpha=0.4, label=f'Alfa (I. druh) = {alpha:.3f}')
    plt.fill_between(x[mask_beta], y1[mask_beta], color='crimson', alpha=0.4, label=f'Beta (II. druh) = {beta:.3f}')
    plt.axvline(critical_val, color='black', linestyle='--', label='Kritická mez')
    plt.title(f'Testování hypotéz: Síla testu (1-β) = {1-beta:.3f}')
    plt.legend()
    plt.show()

def plot_residuals_analysis(mean_error, scale_error, heteroskedasticity, normality):
    """Vizualizace analýzy reziduí (Bílý šum, Homoskedasticita, Normalita)."""
    np.random.seed(42)
    x = np.linspace(0, 100, 200)
    if normality:
        errors = np.random.normal(loc=mean_error, scale=scale_error, size=200)
    else:
        errors = np.random.exponential(scale=scale_error, size=200) - scale_error
    if heteroskedasticity > 0:
        noise_growth = (x / 100) * heteroskedasticity
        errors = errors * (1 + noise_growth)
    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(15, 5))
    ax1.scatter(x, errors, alpha=0.6, color='darkorchid')
    ax1.axhline(0, color='black', linestyle='--', lw=1)
    ax1.set_title('Graf reziduí (Residual Plot)')
    ax1.set_xlabel('Predikovaná hodnota / Čas')
    ax1.set_ylabel('Chyba (Reziduum)')
    ax1.set_ylim(-25, 25)
    sns.histplot(errors, kde=True, ax=ax2, color='darkorchid', alpha=0.4)
    ax2.set_title('Rozdělení reziduí')
    ax2.set_xlabel('Hodnota chyby')
    plt.tight_layout()
    plt.show()

def plot_time_series_decomposition(trend_slope, seasonal_amp, cycle_amp, noise_level):
    """Vizualizace dekompozice s vyznačením měsíců a transparentním překrytím složek."""
    t = np.arange(120)
    dates = pd.date_range(start='2015-01-01', periods=120, freq='MS')

    trend = trend_slope * t
    seasonality = seasonal_amp * np.sin(2 * np.pi * t / 12)
    cycle = cycle_amp * np.sin(2 * np.pi * t / (12 * 4))
    np.random.seed(42)
    noise = np.random.normal(0, noise_level, len(t))

    total = trend + seasonality + cycle + noise

    fig, axes = plt.subplots(5, 1, figsize=(12, 7.5), sharex=True)

    components = [
        (total, 'Výsledná řada (Součet)', 'black'),
        (trend, 'Trend (Dlouhodobý směr)', 'royalblue'),
        (seasonality, 'Sezónnost (Vánoce/Léto - 1 rok)', 'teal'),
        (cycle, 'Cyklus (Hospodářský - 4 roky)', 'goldenrod'),
        (noise, 'Rezidua (Náhodný šum)', 'firebrick')
    ]

    aprils = dates[dates.month == 4]
    octobers = dates[dates.month == 10]

    for i, (ax, (data, title, color)) in enumerate(zip(axes, components)):
        ax.plot(dates, data, color=color, lw=1.5 if i==0 else 1.2)
        ax.set_title(title, fontsize=10, pad=5)
        ax.grid(True, alpha=0.15)
        ax.tick_params(labelsize=8)

        if i == 0:
            ax.plot(dates, trend, color='royalblue', alpha=0.15, lw=1, label='Složka: Trend')
            ax.plot(dates, seasonality + trend[0], color='teal', alpha=0.15, lw=1, label='Složka: Sezónnost')
            ax.plot(dates, cycle + trend[0], color='goldenrod', alpha=0.15, lw=1, label='Složka: Cyklus')

        if i == 0 or i == 2:
            ax.scatter(aprils, data[dates.month == 4], color='limegreen', s=25, zorder=5, label='Duben (Peak)' if i==2 else "")
            ax.scatter(octobers, data[dates.month == 10], color='orangered', s=25, zorder=5, label='Říjen (Trough)' if i==2 else "")
            if i == 2:
                ax.legend(fontsize=7, loc='lower left')

    plt.xlabel('Rok', fontsize=9)
    plt.tight_layout(h_pad=0.4)
    plt.show()

def visualize_stationarity(phi, noise_std):
    """Vizualizace stacionarity: AR(1) proces vs Random Walk."""
    np.random.seed(42)
    n = 200
    y = np.zeros(n)

    for t in range(1, n):
        y[t] = phi * y[t-1] + np.random.normal(0, noise_std)

    res = adfuller(y)
    p_val = res[1]

    plt.figure(figsize=(12, 5))
    plt.plot(y, color='darkblue', lw=1.5)
    plt.axhline(0, color='black', alpha=0.3, linestyle='--')
    plt.ylim(-30, 30)

    status = "STACIONÁRNÍ" if phi < 1 else "NESTACIONÁRNÍ"
    plt.title(f"Proces: AR(1) s φ={phi} | ADF Test p-hodnota: {p_val:.4f}\n" +
              f"Stav: {status} ({'Zamítáme H0' if p_val < 0.05 else 'Nezamítáme H0 - Jednotkový kořen'})")

    plt.xlabel("Čas")
    plt.ylabel("Hodnota")
    plt.show()

